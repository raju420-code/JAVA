public class literals
{
    public static void main(String a[])
    {
        // Literals
        int num1 = 0b101;
        int num2 = 0x7E;
        int num3 = 10_00_00_000;
        double num4 = 55e17;
        char c = 'w';
        c++;
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
        System.out.println(c);
    }

}
